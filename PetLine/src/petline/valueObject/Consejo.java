package petline.valueObject;

public class Consejo {

	private int idConsejo;
	private String titulo;
	private String texto;
	private int edadDesde;
	private int edadHasta;
	private int pesoDesde;
	private int pesoHasta;
	private int idTamaño;
	private int idRaza;

	public int getIdConsejo() {
		return idConsejo;
	}

	public void setIdConsejo(int idConsejo) {
		this.idConsejo = idConsejo;
	}

	public String getTitulo() {
		return titulo;
	}

	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}

	public String getTexto() {
		return texto;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}

	public int getEdadDesde() {
		return edadDesde;
	}

	public void setEdadDesde(int edadDesde) {
		this.edadDesde = edadDesde;
	}

	public int getEdadHasta() {
		return edadHasta;
	}

	public void setEdadHasta(int edadHasta) {
		this.edadHasta = edadHasta;
	}

	public int getPesoDesde() {
		return pesoDesde;
	}

	public void setPesoDesde(int pesoDesde) {
		this.pesoDesde = pesoDesde;
	}

	public int getPesoHasta() {
		return pesoHasta;
	}

	public void setPesoHasta(int pesoHasta) {
		this.pesoHasta = pesoHasta;
	}

	public int getIdTamaño() {
		return idTamaño;
	}

	public void setIdTamaño(int idTamaño) {
		this.idTamaño = idTamaño;
	}

	public int getIdRaza() {
		return idRaza;
	}

	public void setIdRaza(int idRaza) {
		this.idRaza = idRaza;
	}

}
